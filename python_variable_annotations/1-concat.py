#!/usr/bin/env python3
"""A concat function that combines 2 strings."""


def concat(a: str, b: str) -> str:
    """
    Add two strings and return their combination.

    Args:
    a (string): The first number.
    b (string): The second number.

    Returns:
    string: The combination of the 2 strings.
    """
    return a + b
